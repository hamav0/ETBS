CREATE TABLE Аккаунты (
    Логин NVARCHAR(50) NOT NULL UNIQUE,
    Почта NVARCHAR(100) NULL, -- что если ввести только почту? лучше сделать только логин обязательным.
    Номер VARCHAR(50) NULL,
    Пароль BINARY(32) NOT NULL
);
CREATE UNIQUE NONCLUSTERED INDEX Почта_Unique
ON Аккаунты(Почта)
WHERE Почта IS NOT NULL;
GO
CREATE UNIQUE NONCLUSTERED INDEX Номер_Unique
ON Аккаунты(Номер)
WHERE Номер IS NOT NULL;
GO



CREATE TABLE Билеты ( 
    Заказ_ИД NVARCHAR(50) PRIMARY KEY,
    Логин NVARCHAR(50) NOT NULL,
    Событие_ИД NVARCHAR(50) NOT NULL,
    
    -- Одно из полей будет заполнено, второе NULL:
    Место_ИД NVARCHAR(50) NULL, -- Для билетов с конкретными креслами
    Зона_ИД NVARCHAR(50) NULL,  -- Для билетов на танцпол/фан-зону

    Сумма_Оплаты MONEY NOT NULL,
    Дата_Покупки DATETIME DEFAULT GETDATE(),
    
    FOREIGN KEY (Логин) REFERENCES Аккаунты(Логин),
    FOREIGN KEY (Событие_ИД) REFERENCES События(Событие_ИД)
    -- Внешние ключи к местам и зонам лучше настраивать осторожно, 
    -- так как составные ключи требуют проброса Планировка_ИД
);

