begin
-- лучше хранить список каких мест к какой категории цен относится, если место не относится то место не продаётся.
-- "если место не относится то место не продаётся" работает только когда не снесли стол или место, как на кроп арене 4 стол, иначе менять планировку

-- Определяет, какие вообще категории билетов есть на конкретном мероприятии.
CREATE TABLE Событие_Категории (
    Событие_ИД NVARCHAR(50) NOT NULL,
    Категория_ИД INT NOT NULL,       -- Локальный номер: 1, 2, 3...
    Название NVARCHAR(100) NOT NULL, -- "Танцпол", "Балкон", "VIP"
    Цвет VARCHAR(50) NOT NULL,       -- HEX/RGBA для перекраски кнопок в WinForms
    Описание NVARCHAR(MAX) NULL,
    
    PRIMARY KEY (Событие_ИД, Категория_ИД), -- Идеальный ключ для привязки мест!
    FOREIGN KEY (Событие_ИД) REFERENCES События(Событие_ИД)
);

CREATE TABLE Ценовая_Политика (
    Событие_ИД NVARCHAR(50) NOT NULL,
    -- я разделил привязку Цен к Событиям так как для кино-сеансов Ценовая_политика на места может не меняться годами, а сеасов за день може быть штук 60.
    -- Может быть поэтому афиша имеет отдельный модуль, и структуру БД к ней, для кино-сеансов
    Категория_ИД INT NOT NULL,
    -- Пример тип 1: 5000 руб, синий цвет, актульно до дата(14 дней до начала события), описание. И так далее
    Цена MONEY NOT NULL,
    Актуальность DATETIME NOT NULL, -- Актуально ДО этого момента времени
    -- Актуальность даёт возможность делать меньшую цену задолго до концерта, и большУю перед концертом
    FOREIGN KEY (Событие_ИД) REFERENCES События(Событие_ИД)
);
end
