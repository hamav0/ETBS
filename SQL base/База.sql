begin
-- Таблица информации площадок (зданий, арен, клубов)
CREATE TABLE Площадки (
    Площадка_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(255) NOT NULL, 
    -- расширяя систему - продажа будет и в других странах
    -- Лучше  сделать одну структуру ввода, например через запятую в качестве разделителя
    -- Я сделаю структуру так, чтобы сначала была страна для быстрой сортировки, потом город для сортировки
    --  1 - Страна, 2 - город, 3 - улица, 4 - дом
    -- Пример: Грузия, г. Тбилиси, просп. Акакия Церетели, 2
    Адрес NVARCHAR(MAX) NOT NULL,
    Номер NVARCHAR(50) NULL, -- 50 символов если вдруг будет номер телефона другой страны
    Описание NVARCHAR(MAX) NULL,
    -- Планировка_ИД отображает пустую планировку, без функций кнопок, ну если я сделаю странички площадок.
    Планировка_ИД NVARCHAR(50) NOT NULL,
);

-- Таблица Информации
CREATE TABLE Информация (
    Информация_ИД NVARCHAR(50) PRIMARY KEY, -- сокр название и ИД номер, если вдруг что изменится
    -- для сеансов же просто номера, как на кинопоиске (там кстати уже больше 5 000 000 записей)
    Название NVARCHAR(255) NOT NULL,
    Оригинальное_Название NVARCHAR(255) NULL, -- хранит оригинальное название, если есть, например Backrooms
    Возрастной_Рейтинг VARCHAR(10) NULL,
    Продолжительность_Минут INT NULL, 
    Описание NVARCHAR(MAX) NULL,
    Год_Производства DATE NULL,
    -- с одной стороны - в разных городах и местах разные условия, потому продолжительность может быть не постоянной
    -- с другой стороны каждых раз заново её записывать, когда некоторые могу её просто не указать, ну хз хз, так оставлю
    -- с третьей стороны - у кино есть продолжительность, с ней не ошибиться, а уже События согласовывают исполнители и площадки - это их ошибки будут
    
    -- Изображение_ИД NVARCHAR(50) DEFAULT 0,
);

/* 
CREATE TABLE Изображения (
    Изображение_ИД NVARCHAR(50) PRIMARY KEY,
    Изображение VARBINARY(MAX) NOT NULL
        -- Дополнительно можно сделать и баннеры, и даже ссылку на видео
);
