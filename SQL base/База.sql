-- Основная таблица Событий (Афиша)
CREATE TABLE События (
    Событие_ИД NVARCHAR(50) PRIMARY KEY, -- кино - просто цифры
    Информация_ИД NVARCHAR(50) NOT NULL, -- информация о релизе - просто цифры

    Дата_Время DATETIME NOT NULL, -- Дата_Время проведения
    Площадка_ИД INT NOT NULL, -- инфа о месте
    Номер_Зала TINYINT DEFAULT 0,
    -- если у площадки используется нумерация залов с использованием символов, ну тогда анлак
    Планировка_ИД NVARCHAR(50) NOT NULL, -- схема зала/афметеатра и тп
    -- схема
    FOREIGN KEY (Площадка_ИД) REFERENCES Площадки(Площадка_ИД),
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    -- FOREIGN KEY (Планировка_ИД) REFERENCES Планировки_Зон(Планировка_ИД)
);
-- теги
begin
-- Таблица Тегов
CREATE TABLE Тип (
    Тип_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(50) NOT NULL UNIQUE -- (концерт, трибьют, кино)
);
-- Таблица Личностей, для концертов определённых личностей
CREATE TABLE Личности (
    Личность_ИД NVARCHAR(10) PRIMARY KEY, -- инициалы и роль, ЩДСП
    Имя NVARCHAR(50) NOT NULL UNIQUE,
    Роль NVARCHAR(50) NOT NULL -- режисёр или исполнитель
);
-- Таблица Стран источников
CREATE TABLE Страны (
    Страна_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(255) NOT NULL
);
-- Таблица Дистрибьюторов
CREATE TABLE Дистрибьюторы (
    Дистрибьютор_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(255) NOT NULL
);

-- Таблица Форматов
CREATE TABLE Форматы (
    Формат_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(50) NOT NULL UNIQUE
);

-- Таблица Жанров
CREATE TABLE Жанры (
    Жанр_ИД INT PRIMARY KEY IDENTITY(1,1),
    Название NVARCHAR(50) NOT NULL UNIQUE
);

-- Таблица Актеров
CREATE TABLE Актеры (
    Актер_ИД INT PRIMARY KEY IDENTITY(1,1),
    Полное_Имя NVARCHAR(200) NOT NULL
);

-- Таблица Режиссеров
CREATE TABLE Режиссеры (
    Режиссер_ИД INT PRIMARY KEY IDENTITY(1,1),
    Полное_Имя NVARCHAR(200) NOT NULL
);
-- ТУТ ДАЛЕЕ Я НЕ ПОМЕНЯЛ "Релиз" НА "Информация", НЕВКОЕМ СЛУЧАЕ НЕ ПУТАТЬ
-- Актёры, жанры, форматы, дестрибьютеры, страны применимы НЕ ТОЛЬКО К КИНО, например они применимы и для театров,
-- если я неошибаюсь в японии есть 3D театр, с движной сценой
begin
-- Релиз - Страна
CREATE TABLE Релиз_Страны (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Страна_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Страна_ИД) REFERENCES Страны(Страна_ИД)
);

-- Релиз - Дистрибьютор
CREATE TABLE Релиз_Дистрибьюторы (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Дистрибьютор_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Дистрибьютор_ИД) REFERENCES Дистрибьюторы(Дистрибьютор_ИД)
);

-- Релиз - Формат
CREATE TABLE Релиз_Форматы (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Формат_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Формат_ИД) REFERENCES Форматы(Формат_ИД)
);

-- Релиз - Жанр
CREATE TABLE Релиз_Жанры (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Жанр_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Жанр_ИД) REFERENCES Жанры(Жанр_ИД)
);

-- Релиз - Актер
CREATE TABLE Релиз_Актеры (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Актер_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Актер_ИД) REFERENCES Актеры(Актер_ИД)
);

-- Релиз - Режиссер
CREATE TABLE Релиз_Режиссеры (
    Информация_ИД NVARCHAR(50) NOT NULL,
    Режиссер_ИД INT NOT NULL,
    FOREIGN KEY (Информация_ИД) REFERENCES Информация(Информация_ИД),
    FOREIGN KEY (Режиссер_ИД) REFERENCES Режиссеры(Режиссер_ИД)
);
-- Связь Событие - Тип 
CREATE TABLE Событие_Типы (
    Событие_ИД NVARCHAR(50) NOT NULL,
    Тип_ИД INT NOT NULL,
    PRIMARY KEY (Событие_ИД, Тип_ИД),
    FOREIGN KEY (Событие_ИД) REFERENCES События(Событие_ИД),
    FOREIGN KEY (Тип_ИД) REFERENCES Тип(Тип_ИД)
);
-- Связь Событие - Личность 
CREATE TABLE Событие_Личность (
    Событие_ИД NVARCHAR(50) NOT NULL,
    Личность_ИД NVARCHAR(10) NOT NULL,
    PRIMARY KEY (Событие_ИД, Личность_ИД),
    FOREIGN KEY (Событие_ИД) REFERENCES События(Событие_ИД),
    FOREIGN KEY (Личность_ИД) REFERENCES Личности(Личность_ИД)
);
end
end
