--- 3. УНИКАЛЬНАЯ ПЛАНИРОВКА И ГЕОМЕТРИЯ ЗОН
--- ==========================================
begin

--- для начала МНЕ надо выделеть все сущности, так как я теряюсь, сейчас сделаю, чтобы не терялись другие
--- ТЕКСТ МОЖЕТ ВВОДИТЬ В ЗАБЛУЖДЕНИЕ, ОСТЕРЕГАЙТЕСЬ ОШИБОК И ПРОЧИХ ИЗМЕНЕНИЙ
-- Первое это зоны, а вот их атрибуты:
-- Закраска - это координаты для закраски. В приложении: всегда белая, имеет свои Координаты_Полигона, которые будут закрашены
-- Наведение - это координаты для функции при наведении. В приложении: при наведении показывает зону своих Координаты_Полигона, также отображает цену от и до всех мест этой зоны, а также количество доступных мест (кроме танцполов и тп)
-- Начальные координаты X Y

-- Текста:
-- Текст - отображаемый текст. (название зоны, нумерация рядов)
-- Координаты X Y
-- Как отображать - тут должен быть тип отображения, текст может идти сверху вниз (Portrait), или "по обычному" (Landscape) (частично неактульно)
-- Размер текста

-- Места:
-- ИД места
-- Ряд
-- Координаты X Y
-- Тип_Цены - связано Ценовая_Политика, от туда берётся цвет и цена, сортировать цены по зонам будет некорректно
-- Принадлежность к зоне - помогает не вычислять на стороне приложения
-- Маскимум мест - функция кнопки для покупки билетов и танцпола и тп одна и та же, вычисляя количество купленных мест с "ИД места" приложение перестанет генерировать эту кнопку (неактульно)

-- Разделительная графика (полосы разделения танцпола и фан-зоны, или оторажение стола):
-- Координаты X Y
-- Координаты_Полигона


CREATE TABLE Планировки_Зон (
    Планировка_ИД NVARCHAR(50) NOT NULL,-- Внутренний числовой ИД для связей в БД
    Зона_ИД NVARCHAR(50) NOT NULL,      -- код группировки, например: 'КРД-КА-0'
    Название_Зоны NVARCHAR(100) NULL,   -- "Балкон", "Танцпол", "Сектор 3"
    -- как приложение поймёт, что нужно нарисовать линию, а не просто закрасить зону?
     -- если поле пусто, то это информация о зарисовке
    Тип_Зоны VARCHAR(20) NOT NULL, -- 'Места' (с креслами) или 'Танцпол' (Танцпол без кресел) Линия — просто серая кривая Текст — статический текст на схеме,
    Коорд_X INT NOT NULL,             -- Координата X
    Коорд_Y INT NOT NULL,             -- Координата Y
    Координаты_Полигона VARCHAR(MAX) NULL,  -- Координаты точек для GraphicsPath (X1,Y1;X2,Y2...)
    Ориентация_Текста VARCHAR(15) DEFAULT 'Landscape',  -- 'Landscape' (обычный) или 'Portrait' (сверху вниз)
    Размер_Текста INT DEFAULT 12,
    PRIMARY KEY (Планировка_ИД, Зона_ИД) -- Составной первичный ключ
);

CREATE TABLE Планировки_Мест (
    Планировка_ИД NVARCHAR(50) NOT NULL, 
    Зона_ИД NVARCHAR(50) NOT NULL,
    Место_ИД NVARCHAR(50) NOT NULL, -- Составной ИД, например: 'R1_M10'
    Ряд INT NOT NULL,
    Номер INT NOT NULL,
    Коорд_X INT NOT NULL,              
    Коорд_Y INT NOT NULL, 
    FOREIGN KEY (Планировка_ИД, Зона_ИД) REFERENCES Планировки_Зон(Планировка_ИД, Зона_ИД)
);

end

begin
-- Если записи о месте в этой таблице нет — оно не выводится в интерфейсе 
-- или выводится серым (как недоступное для продажи).
CREATE TABLE Событие_Места_Категории (
    Событие_ИД NVARCHAR(50) NOT NULL,
    Планировка_ИД NVARCHAR(50) NOT NULL,
    Место_ИД NVARCHAR(50) NOT NULL,
    Категория_ИД INT NOT NULL, 
    
    -- ключ ссылается на точный Primary Key таблицы Событие_Категории
    FOREIGN KEY (Событие_ИД, Категория_ИД) REFERENCES Событие_Категории(Событие_ИД, Категория_ИД)
);
-- Лимиты танцпола теперь привязаны к планировке
CREATE TABLE Событие_Лимиты_Зон (
    Событие_ИД NVARCHAR(50) NOT NULL,
    Планировка_ИД NVARCHAR(50) NOT NULL,
    Зона_ИД NVARCHAR(50) NOT NULL,
    Макс_Мест INT NOT NULL,
    
    PRIMARY KEY (Событие_ИД, Планировка_ИД, Зона_ИД),
    FOREIGN KEY (Планировка_ИД, Зона_ИД) REFERENCES Планировки_Зон(Планировка_ИД, Зона_ИД)
);
end

